#INPUT NUMBER PRINT A DAY(MONDAY....TO...SUNDAY)
Number_of_Day=int(input("Enter a number[1 To 7]="))
if Number_of_Day==1:
    print("MONDAY")
elif Number_of_Day==2:
    print("TUESDAY")
elif Number_of_Day==3:
    print("WEDNESDAY")
elif Number_of_Day==4:
    print("THURSDAY")
elif Number_of_Day==5:
    print("FRIDAY")
elif Number_of_Day==6:
    print("SATURDAY")
elif Number_of_Day==7:
    print("SUNDAY")
else:
    print("INVAILD INPUT PLEASE RETRY")